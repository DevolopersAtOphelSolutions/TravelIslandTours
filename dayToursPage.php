Nuwara Eliya Day Excursion
In Nuwara- Eliya just over Kandy exists very cold weather throughout the year. Where you get very high mountain tables and flower gardens. Lakes where could do boat sailing and in the mid of the city you be able to visit Gregory park and Lake. with scenic environment. As well you feel very free and calm in a peaceful atmosphere will feel you in a homely type surrounding.

