Sigiriya Day Excursion
The Sigiriya village is very small city of Mathale District, Sigiriya would be that of a rock rising up like a lone sentinel from the surrounding plane. It has a quiet dignity and carries on its summit a royal palace, on its rock walls 5th fifth century paintings and an especially prepared mirror wall scribbled with ancient poetry.

Visit Sigiriya Lion Rock / Sigiriya Village tour through catamaran boat and bullock cart


Galle Day Excursion
In down south of Sri lanka there is a Sub capital city called Galle. In 17th century when Dutch rule Sri Lanka in the beach side they have built a huge fortress in this city there you can visit museum – Juridical courtside and many other things and meet a different culture with the other part to the Island.

Visit Turtle farm and Hatchery / Madu River Boat Trip / Tsunami Photo Museum / Galle Dutch Fort.

Yala Day Excursion
Yala National Park with an area of 1259 sq km is the biggest National Park in Srilanka. The Park is located 309 km south of Colombo on the southeast of the island. The national park is divided into Yala West (also called Ruhuna) and Yala East. Yala West or Ruhuna National Park is one of the best parks in the world to observe and photograph leopards. There are about 35 leopards in the park, which probably is the highest density anywhere in the world. Other animals that can be seen in the park are elephants,  sloth bear, spotted deer, barking deer, mouse deer, toque monkey, mongoose and crocodiles. Yala East contains a large variety of
water birds.

Visit Weligama Stilt Fishing / Yala National Park Jeep Safari

Udawalawe Day Excursion
The park is situated in south of Central Highlands. At the center of the park lies the Udawalawe Reservoir. Udawalawe National Park established in the year 1972, An area 30,821, resembles an African game park: it is mainly thorny-shrub jungle with grasslands. It is the best place in Sri Lanka to see wild Asian Elephants throughout the year and  out of  main attraction of the park are Water Buffalo, Wildboar, Spotted Deer, Sambur Deer, Jackal, Samber, Black-naped hare, mongooses, bandicoots, foxes,Endemic Toque Macaque and Gray Langurs.

Visit Udawalawe National Park Jeep Safari


Kitulgala Day Excursion
The Kithulgala village is one of very small unspoiled village in Sri Lanka. As at Today, Kitulgala is famous for whitewater rafting. It is known as a only location for whitewater rafting destination in South East Asia. it is well-known destination for ornithologists, adventure seekers and romantics. There are many opportunities to engage in nature adventures, jungle trekking, outdoor camping, mountain biking and bird watching.

White Water Rafting Kitulgala – (Grade 3-4, 7 rapids)


